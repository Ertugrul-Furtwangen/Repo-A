package einfachso;

public class Car2 {
	String color;
	int kw;
	int km;
	
	public Car2(String color, int kw, int km){
		this.color=color;
		this.kw=kw;
		this.km=km;
	}
	
	public String getColor(){
		return this.color;
	}

	public int getKw(){
		return this.kw;
	}
	
	public int getKm(){
		return this.km;
	}
	
	

}
